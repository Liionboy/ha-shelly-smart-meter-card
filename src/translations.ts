// Detect language from HA or config
export function detectLang(hass: HomeAssistant, configLang?: string): Lang {
  if (configLang && configLang in translations) return configLang as Lang;
  const haLang = (hass.language || hass.locale?.language || 'en').toLowerCase();
  const short = haLang.split('-')[0] as Lang;
  if (short in translations) return short;
  return 'en';
}

// Translate with variable substitution
export function t(lang: Lang, key: string, vars?: Record<string, string | number>): string {
  let text = translations[lang]?.[key] || translations.en[key] || key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      text = text.replace(`{${k}}`, String(v));
    }
  }
  return text;
}

